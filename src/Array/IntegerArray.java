package Array;

public class IntegerArray {

    public static void main(String[] args) {


        int[] intArray = {2,34,5,65,75,8,6978,3};




        System.out.println(intArray[4]);

        intArray[4] = 90;
        System.out.println(intArray[4]);

        System.out.println(intArray.length);



        for (int i : intArray)
        {
            System.out.print(i + " ");
        }






    }
}
